package Convert;

public class currency {
    private currency(int RUB, int USD, int EUR, int CNY, int RSD, int NOK) {
        this.RUB = RUB;
        this.USD = USD;
        this.EUR = EUR;
        this.CNY = CNY;
        this.RSD = RSD;
        this.NOK = NOK;
    }

    public int RUB;

    public int USD;

    public int EUR;

    public int CNY;

    public int RSD;

    public int NOK;

    public int getRUB() {
        return RUB;
    }

    public int getUSD() {
        return USD;
    }

    public int getEUR() {
        return EUR;
    }

    public int getCNY() {
        return CNY;
    }

    public int getRSD() {
        return RSD;
    }

    public int getNOK() {
        return NOK;
    }
}
